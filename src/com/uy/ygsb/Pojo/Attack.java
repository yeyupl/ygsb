package com.uy.ygsb.Pojo;

public class Attack {

	private int id, type, loseMp, loseHp;

	public void setId(int id) {
		this.id = id;
	}

	public int getId() {
		return id;
	}

	public void setType(int type) {
		this.type = type;
	}

	public int getType() {
		return type;
	}

	public void setLoseMp(int loseMp) {
		this.loseMp = loseMp;
	}

	public int getLoseMp() {
		return loseMp;
	}

	public void setLoseHp(int loseHp) {
		this.loseHp = loseHp;
	}

	public int getLoseHp() {
		return loseHp;
	}

}
